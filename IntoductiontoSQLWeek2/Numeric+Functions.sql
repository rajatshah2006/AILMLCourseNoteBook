-- -------------------------------------
-- NUMERICAL FUNCTIONS
-- -------------------------------------
-- ADDITION, SUBTRACTION, MULTIPLICATION AND DIVISION
SELECT 20+5 ADDITION, 20-9 SUBTRACTION, 25*10 MULTIPLICATION, 25/5 DIVISION ;

-- MODULUS AND DIVISION
SELECT 25 mod 7 MODULUS, 25 div 7 DIVISION;

-- CEIL AND FLOOR
SELECT CEIL(57.0001) CEIL_1, CEIL(57.898) CEIL_2;
SELECT FLOOR(57.0001) FLOOR_1, FLOOR(57.898) FLOOR_2 ;

-- ROUND AND TRUNCATE
SELECT ROUND(57.4982,2) VALUE_1, ROUND(57.898,2) VALUE_2 ;
SELECT TRUNCATE(57.4982,2) VALUE_1, TRUNCATE(57.898,2) VALUE_2;

-- SQUARE ROOT, POWER AND ABSOLUTE
SELECT SQRT(121) SQUARE_ROOT;
SELECT POWER(3,2) POWER;
SELECT ABS(-324) ABSOLUTE;


